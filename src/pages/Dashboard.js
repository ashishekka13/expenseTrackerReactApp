export default function Dashboard() {
  const [{}, makeLogoutRequest] = useLogoutRequest();

  const handlelogoutClick = () => {
    makeLogoutRequest().then(({ status }) => {
      sessionStorage.clear();
    });
  };

  const classes = useStyles();
  const [open, setOpen] = React.useState(false);
  const handleDrawerOpen = () => {
    setOpen(true);
  };
  const handleDrawerClose = () => {
    setOpen(false);
  };
  const fixedHeightPaper = clsx(classes.paper, classes.fixedHeight);

  const adminData = JSON.parse(sessionStorage.getItem(STORAGE_KEYS.ADMIN));

  const { accessLevel: userSessionAccessLevel } = adminData || {};

  const renderRoutes = (menuItems) =>
    menuItems.map(({ exact, path, component, key }) => (
      <PrivateRoute {...{ exact, path, component, key }} />
    ));

  return (
    <div className={classes.root}>
      <CssBaseline />

      <AppBar
        position="absolute"
        className={clsx(classes.appBar, open && classes.appBarShift)}
      >
        <Toolbar className={classes.toolbar}>
          <IconButton
            edge="start"
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            className={clsx(
              classes.menuButton,
              open && classes.menuButtonHidden
            )}
          >
            <MenuIcon />
          </IconButton>
          <Typography
            component="h1"
            variant="h6"
            color="inherit"
            noWrap
            className={classes.title}
          >
            My Expense Tracker
          </Typography>
          <Link to={ROUTES.LOGIN}>
            <IconButton color="inherit">
              <LogoutIcon />
            </IconButton>
          </Link>
        </Toolbar>
      </AppBar>
      <Drawer
        variant="permanent"
        classes={{
          paper: clsx(classes.drawerPaper, !open && classes.drawerPaperClose),
        }}
        open={open}
      >
        <div className={classes.toolbarIcon}>
          <IconButton onClick={handleDrawerClose}>
            <ChevronLeftIcon />
          </IconButton>
        </div>
        <Divider />
        <List>
          <MainListItems logoutClick={handlelogoutClick} />
        </List>
        <Divider />
        <List>{userSessionAccessLevel >= 5 && secondaryListItems}</List>
      </Drawer>

      <main className={classes.content}>
        <div className={classes.appBarSpacer} />
        {renderRoutes(routes)}
      </main>
    </div>
  );
}

